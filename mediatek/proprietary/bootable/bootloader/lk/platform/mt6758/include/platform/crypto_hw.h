#ifndef _CRYPTO_HW_H_
#define _CRYPTO_HW_H_

#include <platform/mt_typedefs.h>
#include <platform/sec_status.h>
#include <sys/types.h>

#define SHA256_HASH_SIZE   (32)
#define AES_BLK_SIZE       (16)

/* memory map of crypto hw shared memory */
typedef struct {
	unsigned char hash[SHA256_HASH_SIZE];
	unsigned char last_block[AES_BLK_SIZE];
} CRYPTO_HW_SHARE_MEM;

unsigned int calc_hash_hw(unsigned char *buf, unsigned int buf_sz, unsigned int padding_sz, unsigned char *hash, unsigned int hash_sz);
unsigned int sha256hw_init(void);
unsigned int sha256hw_process(const unsigned char *in, unsigned long len);
unsigned int sha256hw_done(unsigned char *hash);

#endif /* _CRYPTO_HW_H_ */

